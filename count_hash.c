#include "fillit.h"

int		count_hash(char *a)
{
	int		i;
	int		hash;

	i = 0;
	hash = 0;
	while (a[i] != '\0')
	{
		if (a[i] == '#')
			hash++;
		i++;
	}
	return (hash);
}
