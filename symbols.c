#include "fillit.h"

int		symbols(char *a)
{
	int	i;

	i = 0;
	while (a[i] != '\0')
	{
		if (a[i] == '#' || a[i] == '.' || a[i] == '\n')
			i++;
		else
			return (0);
	}
	return (1);
}
